    public void llenarInformacion() {
        if (svHumanos.getHumanoSeleccionado() != null) {
            Humano humano = svHumanos.getHumanoSeleccionado();
            txtId.setText(String.valueOf(humano.getId()));
            txtNombre.setText(humano.getNombre());
            txtApellido.setText(humano.getApellido());
            txtPeso.setText(String.valueOf(humano.getPeso()));
            
            String sexo = humano.getSexo();
            if (sexo.equals("M")) {
                optMasculino.setSelected(true);
            }else{
                optFemenino.setSelected(true);
            }
            cmbDepartamento.getModel().setSelectedItem(humano.getDepartamento());
            lstMunicipio.setSelectedValue(humano.getMunicipio(), true);            
        }
    }


    public void guardarNuevoHumano() {
        try {
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            double peso = Double.parseDouble(txtPeso.getText());

            String sexo = "F";
            if (optMasculino.isSelected()) {
                sexo = "M";
            }

            String departamento = (String) cmbDepartamento.getSelectedItem();
            String municipio = lstMunicipio.getSelectedValue();

            Humano humanoNuevo = new Humano(id, nombre, apellido, peso, sexo, departamento, municipio);

            svHumanos.guardar(humanoNuevo);

            JOptionPane.showMessageDialog(this, "HUmano almacenado " + humanoNuevo.toString());

            this.dispose(); //cierra el formulario
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                     e.getMessage(),
                     "Error en la aplicacion",
                     JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    public void actualzarHumano() {
        try {
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            double peso = Double.parseDouble(txtPeso.getText());

            String sexo = "F";
            if (optMasculino.isSelected()) {
                sexo = "M";
            }

            String departamento = (String) cmbDepartamento.getSelectedItem();
            String municipio = lstMunicipio.getSelectedValue();

            Humano humano = svHumanos.getHumanoSeleccionado();

            humano.setNombre(nombre);
            humano.setApellido(apellido);
            humano.setDepartamento(departamento);
            humano.setMunicipio(municipio);
            humano.setPeso(peso);
            humano.setSexo(sexo);
            //por que no pongo el id? R// por que es el identificador unico del objeto
            
            JOptionPane.showMessageDialog(this, "HUmano actualizado " + humano);

            this.dispose(); //cierra el formulario
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                     e.getMessage(),
                     "Error en la aplicacion",
                     JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (svHumanos.getHumanoSeleccionado()!=null) {
            actualzarHumano();
        }else{
            guardarNuevoHumano();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

